export function getTranslation(lang: Language = 'en') {
  return TRANSLATIONS[lang] || TRANSLATIONS.en;
}

export function t(key: keyof typeof TRANSLATIONS.en, lang: Language = 'en', replacements?: Record<string, string | number>): string {
  const dict = getTranslation(lang);
  let text = dict[key] || TRANSLATIONS.en[key] || String(key);
  
  if (replacements) {
    Object.entries(replacements).forEach(([k, v]) => {
      text = text.replace(new RegExp(`{{${k}}}`, 'g'), String(v));
    });
  }
  
  return text;
}
