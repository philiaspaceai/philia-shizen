// Helper to generate a friendly Japandi-style learning note for long vowels
export function getLongVowelNote(hiragana: string, romaji: string): string {
  // Extract vowel with lines from the romaji
  const accentedVowels = romaji.match(/[āīūēō]/g) || [];
  const uniqueAccents = Array.from(new Set(accentedVowels)).join(", ") || "ō";
  
  return `The word ${hiragana} is read as ${romaji}, not ${romaji.replace(/[āīūēō]/g, v => v.normalize("NFD").replace(/[\u0300-\u036f]/g, ""))}. A letter with a line over it, like ${uniqueAccents}, means the sound is held a little longer. Don't read it too quickly. Hold the sound for a moment so it can be heard clearly. If long and short vowels are mixed up, the meaning of the word can change.`;
}

